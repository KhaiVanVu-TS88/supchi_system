def fast_translate_en_vi(text: str) -> str | None:
    """
    Tra bảng dịch tĩnh O(1). Trả về None nếu không tìm thấy.
    Thử nhiều pattern: exact → lowercase → strip "to " ở đầu.
    """
    if not text:
        return None

    # 1. Exact match
    if text in EN_TO_VI:
        return EN_TO_VI[text]

    # 2. Lowercase
    lower = text.lower().strip()
    if lower in EN_TO_VI:
        return EN_TO_VI[lower]

    # 3. Bỏ "to " ở đầu: "to learn" → "learn"
    stripped = lower.removeprefix("to ").strip()
    if stripped in EN_TO_VI:
        return EN_TO_VI[stripped]

    # 4. Partial match: nếu text bắt đầu bằng key đã biết
    for key, val in EN_TO_VI.items():
        if lower.startswith(key + " ") or lower.startswith(key + ","):
            return val

    return None